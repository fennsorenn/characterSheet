import pw from '/opt/node22/lib/node_modules/playwright/index.js';
const { chromium } = pw;
const b = await chromium.launch();
const ctx = await b.newContext();
const p = await ctx.newPage();
await p.setViewportSize({ width: 1100, height: 1000 });
await p.goto('http://localhost:3000/', { waitUntil: 'networkidle' });
await p.setInputFiles('input[type=file]', '/tmp/5etools.zip');
await p.waitForSelector('.data-toggle:has-text("Data ✓")', { timeout: 60000 });
const strBox = p.locator('.ability', { hasText: 'Str' }).first();
const si = strBox.locator('input'); await si.click(); await si.fill('16'); await si.press('Enter');
await p.fill('.search input', 'headband of intellect');
await p.waitForSelector('.results li .add');
await p.locator('.results li', { hasText: 'Headband of Intellect' }).first().locator('.add').click();
await p.waitForTimeout(150);
const inv = p.locator('.cell', { hasText: 'Inventory' }).locator('li', { hasText: 'Headband of Intellect' }).first();
await inv.locator('input[type=checkbox]').check();
await inv.locator('button.attune').click();
// also a belt for STR
await p.fill('.search input', 'belt of hill giant');
await p.waitForSelector('.results li .add');
await p.locator('.results li', { hasText: 'Belt of Hill Giant Strength' }).first().locator('.add').click();
await p.waitForTimeout(150);
const inv2 = p.locator('.cell', { hasText: 'Inventory' }).locator('li', { hasText: 'Belt of Hill Giant' }).first();
await inv2.locator('input[type=checkbox]').check();
await inv2.locator('button.attune').click();
await p.waitForTimeout(250);
const abil = await p.locator('.cell', { hasText: 'Ability Scores' }).first().boundingBox();
await p.screenshot({ path: '/tmp/icon2.png', clip: { x: abil.x, y: abil.y, width: abil.width, height: abil.height } });
await b.close();
