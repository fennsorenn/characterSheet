import pw from '/opt/node22/lib/node_modules/playwright/index.js';
const { chromium } = pw;
const b = await chromium.launch();
const ctx = await b.newContext();
const p = await ctx.newPage();
await p.setViewportSize({ width: 1300, height: 1400 });
const errors = [];
p.on('console', m => { if (m.type()==='error') errors.push(m.text()); });
p.on('pageerror', e => errors.push(String(e)));
await p.goto('http://localhost:3000/', { waitUntil: 'networkidle' });
await p.setInputFiles('input[type=file]', '/tmp/5etools.zip');
await p.waitForSelector('.data-toggle:has-text("Data ✓")', { timeout: 60000 });
const lvl = p.locator('.cell', { hasText: 'Defenses' }).locator('.stat', { hasText: 'Level' }).locator('input');
await lvl.click(); await lvl.fill('3'); await lvl.press('Enter');
await p.waitForTimeout(200);
const feat = p.locator('.cell', { hasText: 'Features' });
const featCount0 = await feat.locator('.features li').count();
const subSel = feat.locator('.line', { hasText: 'Fighter subclass' }).locator('select');
const subOpts = await subSel.locator('option').evaluateAll(els=>els.slice(1,4).map(e=>e.textContent));
await subSel.selectOption({ index: 2 });
await p.waitForTimeout(200);
const featCount1 = await feat.locator('.features li').count();
const names = await feat.locator('.features li .fname').evaluateAll(els=>els.map(e=>e.textContent));
console.log('features at L3 (no subclass):', featCount0);
console.log('subclass options sample:', subOpts.join(', '));
console.log('features after subclass:', featCount1);
console.log('feature names:', names.join(', '));
console.log('errors:', errors.length ? errors.slice(0,3):'none');
await b.close();
